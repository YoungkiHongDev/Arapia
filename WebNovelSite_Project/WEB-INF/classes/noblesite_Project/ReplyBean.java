package noblesite_Project;

public class ReplyBean {
	private String resid;
	private String intitle;
	private String resnick;
	private int r_date;
	private String r_content;
	
	public String getResid() {
		return resid;
	}
	public void setResid(String resid) {
		this.resid = resid;
	}
	public String getIntitle() {
		return intitle;
	}
	public void setIntitle(String intitle) {
		this.intitle = intitle;
	}
	public String getResnick() {
		return resnick;
	}
	public void setResnick(String resnick) {
		this.resnick = resnick;
	}
	public int getR_date() {
		return r_date;
	}
	public void setR_date(int r_date) {
		this.r_date = r_date;
	}
	public String getR_content() {
		return r_content;
	}
	public void setR_content(String r_content) {
		this.r_content = r_content;
	}
	
	
}
